Water pollution poses equally serious public health threats, with contaminated water sources linked to numerous diseases and health conditions. Waterborne pathogens, including bacteria, viruses, and parasites, cause diseases such as cholera, typhoid, hepatitis, and dysentery, which remain major health concerns in many developing countries. Chemical contamination of water supplies can result from industrial discharges, agricultural runoff, mining activities, and improper waste disposal.
