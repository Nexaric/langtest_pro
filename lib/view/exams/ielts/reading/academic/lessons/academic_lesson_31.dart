Primates exhibit sophisticated social intelligence, including the ability to understand complex social hierarchies, engage in deceptive behaviors, and demonstrate empathy toward group members. Chimpanzees and bonobos show remarkable abilities in learning human sign language, solving mathematical problems, and using tools in sophisticated ways. These abilities suggest that the cognitive foundations for human intelligence may have deeper evolutionary roots than previously understood.
