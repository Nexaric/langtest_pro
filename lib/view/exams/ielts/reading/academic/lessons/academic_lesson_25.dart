The medieval period saw music become increasingly structured and institutionalized, particularly within the Christian church. Gregorian chant represented one of the earliest forms of organized Western music, with standardized melodies and performance practices that unified Christian worship across Europe. The development of musical notation during this period revolutionized music by enabling composers to record their works precisely and enabling performers to learn music without direct instruction from composers.

